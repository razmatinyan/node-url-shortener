document.querySelector('#gen-btn').addEventListener('click', shortenURL)

function shortenURL() {
    const xhr = new XMLHttpRequest();
    xhr.open("POST", 'https://shorturl-u5pu.onrender.com/', true);
    xhr.setRequestHeader('Content-type', 'application/json; charset=UTF-8');
    xhr.send(
        JSON.stringify({
            url: document.querySelector('#url').value ?? ''
        })
    );
    xhr.onload = function(e) {
        if (this.readyState === XMLHttpRequest.DONE && this.status === 200) {
            const response = JSON.parse(xhr.responseText);
            if ( response.status === 200 ) {
                buildResultBlock(response.data.id, response.data.domain);
            } else {
                buildErrorBlock();
            }
        }
    }
}

function buildResultBlock(id, domain) {
    document.querySelector('#error').classList.remove('active');
    document.querySelector('.result').innerHTML = `Your link is: <span class="short">${domain + id}</span><span class="icon" title="Copy"><svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" class="bi bi-clipboard" viewBox="0 0 16 16"><path d="M4 1.5H3a2 2 0 0 0-2 2V14a2 2 0 0 0 2 2h10a2 2 0 0 0 2-2V3.5a2 2 0 0 0-2-2h-1v1h1a1 1 0 0 1 1 1V14a1 1 0 0 1-1 1H3a1 1 0 0 1-1-1V3.5a1 1 0 0 1 1-1h1v-1z"/><path d="M9.5 1a.5.5 0 0 1 .5.5v1a.5.5 0 0 1-.5.5h-3a.5.5 0 0 1-.5-.5v-1a.5.5 0 0 1 .5-.5h3zm-3-1A1.5 1.5 0 0 0 5 1.5v1A1.5 1.5 0 0 0 6.5 4h3A1.5 1.5 0 0 0 11 2.5v-1A1.5 1.5 0 0 0 9.5 0h-3z"/></svg></span>`;
    addIconListener();
}

function buildErrorBlock() {
    removeIconListener();
    document.querySelector('#error').classList.add('active');
    document.querySelector('.result').innerHTML = ''
}

function addIconListener() {
    document.querySelector('.icon')?.addEventListener('click', listenerHandler);
}

function removeIconListener() {
    document.querySelector('.icon')?.addEventListener('click', listenerHandler);
}

